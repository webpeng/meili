function countdown(oItem, timeEnd) {
	var now = new Date();
	var days = Math.floor((timeEnd - now) / 1000 / 60 / 60 / 24);

	if (days > 0) {
		oItem.getParent('span').dispose();
		return;
	}

	var hours = Math.floor((timeEnd - now) / 1000 / 60 / 60 - (24 * days));
	var minutes = Math.floor((timeEnd - now) / 1000 /60 - (24 * 60 * days) - (60 * hours));
	var seconds = Math.floor((timeEnd - now) / 1000 - (24 * 60 * 60 * days) - (60 * 60 * hours) - (60 * minutes));

	oItem.innerHTML = hours +' 小时 ' + minutes + ' 分 ' + seconds + ' 秒';

	setTimeout(function () {
		if ((days == 0) && (hours == 0) && (minutes == 0) && (seconds == 0)) {
			oItem.getParent('.pro-activitylist').dispose();
			return;
		}

		countdown(oItem, timeEnd);
	}, 1000);
}
