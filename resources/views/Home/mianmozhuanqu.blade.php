function createStoreHtml(data){
    var product_url = data.product_info.url? '/'+data.product_info.url:  '/product-'+data.product_info.product_id+'.html';
    var html = '<a href="'+product_url+'" target="_blank"><i class="icon-over itips">抢光啦!</i></a>';
    return html;
}

function createSecHtml(data){
    var uniqueKey = '';
    var html = '<p class="time" id="seckill_time_'+data.seckill.seckill_id+'_'+uniqueKey+'"><i class="icon iconfont">&#xe620;</i>';
    if(data.seckill.start_time > data.now){
        html += '离开始<span class="day">00</span>天<span class="hour">00</span>时<span class="minute">00</span>分<span class="second">00</span>秒';
    }
    else{
        html += '剩余<span class="day">00</span>天<span class="hour">00</span>时<span class="minute">00</span>分<span class="second">00</span>秒';
    }
    html += '</p>';
    return html;    
}

function createPriceHtml(data){
      var html = '';
      if(data.price.saveprice){
          html += '<span class="count fr">'+data.price.saveprice+'</span>';
      }
      html += '<span class="price tl">￥'+parseFloat(data.price.price).toFixed(1)+'</span>';
       if(data.price.mktprice){
          html += '<span class="dis tl"><del>￥'+parseFloat(data.price.mktprice).toFixed(1)+'</del></span>';
      }
      return html;
}



